    private void jTabbedPane1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MousePressed
        // TODO add your handling code here:
        //System.out.println();
        int indice = jTabbedPane1.getSelectedIndex();
        
        switch(indice){
            case 0: // Panel para registrar o crear nuevos productos
                JPanel panel1 = (JPanel) jTabbedPane1.getComponentAt(indice);
                final InsertarProductos insertar = new InsertarProductos( panel1 );
                insertar.limpiarCampos();
                break;
            case 1: 
                // mostrar todos los articulos
                JPanel panel2 = (JPanel) jTabbedPane1.getComponentAt(indice);
                final MostrarProductos consultar = new MostrarProductos( panel2 );
                consultar.cantidadProductos(productosTabla);
                break;
            case 2: 
                // modificar un articulo de todos los disponibles                
                JPanel panel3 = (JPanel) jTabbedPane1.getComponentAt(indice);
                final EditarProductos editar = new EditarProductos( panel3 );
                editar.limpiarCampos();
                break;
            case 3: 
                // eliminar un articulo de todos los disponibles
                // modificar un articulo de todos los disponibles                
                JPanel panel4 = (JPanel) jTabbedPane1.getComponentAt(indice);
                final EliminarProductos eliminar = new EliminarProductos( panel4 );
                eliminar.limpiarCampos();
                break;
            default: break;
        }
        
       // System.out.println("exite el panel? " + panel);
    }//GEN-LAST:event_jTabbedPane1MousePressed

