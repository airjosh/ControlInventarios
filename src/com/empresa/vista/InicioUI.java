    private void jButtonInsertActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionActionPerformed
        // TODO add your handling code here:
        // boton insertar; un nuevo registro fue presionado
        
        JPanel panel = (JPanel) jTabbedPane1.getComponentAt(0);
        final InsertarProductos insertar = new InsertarProductos( panel );
        boolean isCompleto = insertar.validar();
        if (isCompleto){
            System.out.println( "Inicia la creacion de un objeto Producto> " );
            Producto productoNuevo = insertar.accion();
            if (productoNuevo != null){
                this.productosTabla.add(productoNuevo);
                JOptionPane.showMessageDialog(null, "¡Producto registrado exitosamente!.");
            }
        }
        
        // limpiar los campos
        insertar.limpiarCampos();
        
        // insertar el nuevo registro en el modelo
        insertar.enModelo(this.modelo, this.productosTabla);        
    }//GEN-LAST:event_jButtonInsertActionActionPerformed

